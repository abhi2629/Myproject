    private void acnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_acnActionPerformed
        
        try{
                long ACCU=Long.parseLong(acn.getText());
                database.dbconnect.getUser.setLong(1, ACCU);
                ResultSet rs=database.dbconnect.getUser.executeQuery();
                 
                if(rs.next()){
                    an.setText(rs.getString(1));
                    n.setText(rs.getString(3));
                    java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("dd/MM/yyyy");
                    String sdate=sdf.format(rs.getDate(4));
                    dob.setText(sdate);
                    p.setText(rs.getString(5));
                    adr.setText(rs.getString(6));
                    s.setText(rs.getString(7));
                    c.setText(rs.getString(8));
                    o.setText(rs.getString(9));
                    adhar.setText(rs.getString(10));
                     g.setText(rs.getString(11));
                     ms.setText(rs.getString(12));     
                    mn.setText(rs.getString(13));
                    fn.setText(rs.getString(14));
                    b.setText(rs.getString(15));
                    nm.setText(rs.getString(16));
                    
                    java.awt.Container crt=getContentPane();
                    java.awt.CardLayout cd=(java.awt.CardLayout)crt.getLayout();
                    cd.show(crt, "card3");
                }
                else{
                    JOptionPane.showMessageDialog(null, "Account Doesn't Exists!");
                    acn.setText(null);
                }
        }catch(Exception e){
                    JOptionPane.showMessageDialog(null, "Please Enter Account Number In Number Format!");
        }
        
        

    }//GEN-LAST:event_acnActionPerformed

