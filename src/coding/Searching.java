    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int r=JOptionPane.showConfirmDialog(null,"Delete This Account ?", "Acount Delete Conformation", JOptionPane.YES_NO_OPTION);
        if(r==JOptionPane.YES_OPTION){
        try{
               
                long u=Long.parseLong(an.getText());
                database.dbconnect.deleteUser.setLong(1, u);           
                int x=database.dbconnect.deleteUser.executeUpdate();
                
                if(x !=0){
                     
                    JOptionPane.showMessageDialog(null, "Account Succesfully Deleted !");
                    java.awt.Container crt=getContentPane();
                    java.awt.CardLayout cd=(java.awt.CardLayout)crt.getLayout();
                    cd.show(crt, "card2");
                    acn.setText("");
                
                }else{
                     JOptionPane.showMessageDialog(null, "Error in Deletion !");
                }
                
         }catch(Exception ex){
                 JOptionPane.showMessageDialog(null, ex);
         }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

