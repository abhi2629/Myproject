    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       if(!acn.getText().equals("")){
        try{
                long ACCU=Long.parseLong(acn.getText());
                database.dbconnect.getUser.setLong(1, ACCU);
                ResultSet rs=database.dbconnect.getUser.executeQuery();
                 
                if(rs.next()){
                        
                    na.setText(rs.getString(3));
                    dofb.setDate(rs.getDate(4));
                    phnum.setText(rs.getString(5));
                    adr.setText(rs.getString(6));
                    sta.setText(rs.getString(7));
                    cunty.setSelectedItem(rs.getString(8));
                    occu.setText(rs.getString(9));
                    adda.setText(rs.getString(10));
                        if(rs.getString(11).equals("MALE")){
                                g1.setSelected(true);
                        }
                        else if(rs.getString(11).equals("FEMALE")){
                                g2.setSelected(true);
                        }
                        else
                                g3.setSelected(true);
                        
                                        if(rs.getString(12).equals("MARRIED")){
                                                m1.setSelected(true);
                                         }
                                        else
                                        m2.setSelected(true);
                                    
                    mname.setText(rs.getString(13));
                    fname.setText(rs.getString(14));
                    nmme.setText(rs.getString(16));
                    
                    
                
                }else{
                        gggg.setText("Invalid Account Number !");
                        na.setText("");
                    dofb.setDate(null);
                    phnum.setText("");
                    adr.setText("");
                    sta.setText("");
                    cunty.setSelectedItem("");
                    occu.setText("");
                    adda.setText("");
                        
                                g1.setSelected(false);
                                g2.setSelected(false);
                                g3.setSelected(false);
                        
                                                m1.setSelected(false);
                                                m2.setSelected(false);
                                    
                    mname.setText("");
                    fname.setText("");
                    nmme.setText("");
                }
                
                
        }catch(Exception e){
                    JOptionPane.showMessageDialog(null, e);
        }
       }else{
                gggg.setText("Please Provide the Account Number First!");
       }
    }//GEN-LAST:event_jButton2ActionPerformed

