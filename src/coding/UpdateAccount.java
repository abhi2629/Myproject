    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        int r=JOptionPane.showConfirmDialog(this, "UPDATE ACCOUNT ?", "UPDATION Conformation !", JOptionPane.YES_NO_OPTION);
        if(r == JOptionPane.YES_OPTION){
                try{
                String NAME=na.getText();

                java.util.Date DT=dofb.getDate();

                long PHONE_NUMBER=Long.parseLong(phnum.getText());
                String ADDRESS =adr.getText();
                String STATE=sta.getText();

                String COUNTRY=(String)cunty.getSelectedItem();

                String OCCUPATION=occu.getText();
                long AADHAR_NUMBER =Long.parseLong(adda.getText());

                String GENDER="";
                    if(g1.isSelected()){
                        GENDER = g1.getText();
                    }
                    else if(g2.isSelected()){
                        GENDER = g2.getText();
                    }
                    else
                        GENDER = g3.getText();



                    String MARITAL_STATUS ="";
                    if(m1.isSelected()){
                        MARITAL_STATUS = m1.getText();
                    }
                    else{
                        MARITAL_STATUS = m2.getText();
                    }
                            
                String MOTHER_NAME =mname.getText();
                String FATHER_NAME =fname.getText();
                String NOMINEE =nmme.getText();

                                if(!acn.getText().equals("") &&  !NAME.equals("") && dofb.getDate() !=null && !phnum.getText().equals("")
                                        && !ADDRESS.equals("") && !STATE.equals("") && !COUNTRY.equals("")
                                        && !OCCUPATION.equals("") &&!adda.getText().equals("") && !GENDER.equals("")
                                        && !MARITAL_STATUS.equals("") && !MOTHER_NAME.equals("")
                                        && !FATHER_NAME.equals("") && !NOMINEE.equals(""))
                                {               //code to convert java.util.Date to java.sql.Date
                                    
                                                                java.sql.Date SQL_BIRTH_DATE = new java.sql.Date(DT.getTime());
                                            try{
                                                         long ACCOUNT_NUM=Long.parseLong(acn.getText());
                                                         
                                                        database.dbconnect.updateUser.setString(1, NAME);
                                                        database.dbconnect.updateUser.setDate(2, SQL_BIRTH_DATE);
                                                        database.dbconnect.updateUser.setLong(3, PHONE_NUMBER);
                                                        database.dbconnect.updateUser.setString(4, ADDRESS);
                                                        database.dbconnect.updateUser.setString(5, STATE);
                                                        database.dbconnect.updateUser.setString(6, COUNTRY);
                                                        database.dbconnect.updateUser.setString(7, OCCUPATION);
                                                        database.dbconnect.updateUser.setLong(8, AADHAR_NUMBER);
                                                        database.dbconnect.updateUser.setString(9, GENDER);
                                                        database.dbconnect.updateUser.setString(10, MARITAL_STATUS);
                                                        database.dbconnect.updateUser.setString(11, MOTHER_NAME);
                                                        database.dbconnect.updateUser.setString(12, FATHER_NAME);
                                                        database.dbconnect.updateUser.setString(13, NOMINEE);
                                                        
                                                        database.dbconnect.updateUser.setLong(14, ACCOUNT_NUM);
                                                        
                                                        int v=database.dbconnect.updateUser.executeUpdate();
                                                            if(v !=0){
                                                                    JOptionPane.showMessageDialog(null,"Account Succesfully Updated !");
                                                                    dispose();
                                                            }
                                                            else{
                                                                    gggg.setText("Error in Updation !  check your Account number.");
                                                            }
                                                      
                                            }catch(Exception e){
                                                gggg.setText("Error While Communicating To Database !!");
                                            }
                                }
                                else{
                                    
                                            gggg.setText("PLEASE FILL UP ALL DETAILS CORRECTLY !!");
                                }

                }catch(Exception e)
                {
                    JOptionPane.showMessageDialog(null, e);
                }
        }
        else{
                    
        }
    }//GEN-LAST:event_jButton1ActionPerformed

